public class JavaVeriables {

    public static void main(String[] args) {

        // PRINT SCREEN - EKRAN ÇIKTISI
        System.out.println("Hello World!");

        //Primitive Data Types - İlkel veri tipleri

        byte age = 120;  // -128 ve 127 arasında değerler alır. - It takes values between -128 and 127.
        short dear = -1300; //-32768 ve 32767 arasında değerler alır.  - It takes values between -32768 and 32767.
        // -2,147,483,648 to 2,147,483,647  İNTEGER Data Type - TAMSAYI Veri türü
        int myAge = 21;
        long uzunSayilarİcin = 1313013; // Çok uzun sayıları tutmak için kullanılır - Used to hold very long numbers

        // DATA TYPES - VERİ TİPLERİ

        // The String data type is used to store a sequence of characters (text).
        // String veri türü, bir karakter dizisini (metini) saklamak için kullanılır.
        String myName = "Ibrahim";

        // Sufficient for storing 6 to 7 decimal digits.
        //6-7 Ondalık basamağı olan sayıları depolar.
        float myFloatNumber = 5.99f;

        // Stores a single character/letter or ASCII values.
        // Tek bir karakter / harf veya ASCII değerleri depolar.
        char myNameFirstLetter = 'I';

        //Stores true or false values.
        // Doğru veya yanlış değerleri depolar.
        boolean myBoolean = true;
    }

}
